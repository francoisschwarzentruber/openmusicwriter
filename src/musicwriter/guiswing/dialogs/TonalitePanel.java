    private JToggleButton tonaliteToButton(Tonalite tonalite)
    {
        switch(tonalite.getDiesesNombre())
        {
            case -7: return mnuTonaliteB7;
            case -6: return mnuTonaliteB6;
            case -5: return mnuTonaliteB5;
            case -4: return mnuTonaliteB4;
            case -3: return mnuTonaliteB3;
            case -2: return mnuTonaliteB2;
            case -1: return mnuTonaliteB1;
            case 0: return mnuTonalite0;
            case 7: return mnuTonaliteD7;
            case 6: return mnuTonaliteD6;
            case 5: return mnuTonaliteD5;
            case 4: return mnuTonaliteD4;
            case 3: return mnuTonaliteD3;
            case 2: return mnuTonaliteD2;
            case 1: return mnuTonaliteD1;
            default: return null;
        }
    }



public void setTonalite(Tonalite tonalite)
{
    tonaliteToButton(tonalite).setSelected(true);
    this.tonalite = tonalite;
}

