    static private int JToggleButtonToInt(JToggleButton bouton)
    {
        if(bouton.getName().equals("mnuTonalite0"))
            return 0;
        else if (bouton.getName().startsWith("mnuTonaliteD"))
              return  Integer.parseInt(bouton.getName().substring(bouton.getName().length()-1));
        else
              return   -Integer.parseInt(bouton.getName().substring(bouton.getName().length()-1));

    }

